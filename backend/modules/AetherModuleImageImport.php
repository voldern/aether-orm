class AetherModuleImageImport extends AetherModule {
    /**
     * Render module
     *
     * @access public
     * @return string
     */
    public function run() {
        $tpl = $this->sl->getTemplate();
        $config = $this->sl->get('aetherConfig');
        $pid = $config->getUrlVar('product_id');
        $tpl->set('id', $pid);
        
        return $tpl->fetch('image/import.tpl');
    }
}
